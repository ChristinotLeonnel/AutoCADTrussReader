def generer_si_necessaire() -> None:
    """Crée le dossier icons/ et écrit les SVGs s'ils n'existent pas."""
    dossier = Path(__file__).parent / "icons"
    dossier.mkdir(exist_ok=True)
    for nom, xml in SVGS.items():
        chemin = dossier / nom
        if not chemin.exists():
            with open(chemin, "w", encoding="utf-8") as f:
                f.write(xml)
